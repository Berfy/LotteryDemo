    /**
     * 校验银行号 是否合法
     *
     * @param cardId
     * @return
     */
    public static boolean checkBankCard(String cardId) {
        if (TextUtils.isEmpty(cardId)) {
            return false;
        }
        char bit = getBankCardCheckCode(cardId
                .substring(0, cardId.length() - 1));
        if (bit == 'N') {
            return false;
        }
        boolean isBankCard = cardId.charAt(cardId.length() - 1) == bit;
        return isBankCard;
    }

    /**
     * 从不含校验位的银行号采用 Luhm 校验算法获得校验位
     *
     * @param nonCheckCodeCardId
     * @return
     */
    public static char getBankCardCheckCode(String nonCheckCodeCardId) {
        if (nonCheckCodeCardId == null
                || nonCheckCodeCardId.trim().length() == 0
                || !nonCheckCodeCardId.matches("\\d+")) {
            // 如果传的不是数据返回N
            return 'N';
        }
        char[] chs = nonCheckCodeCardId.trim().toCharArray();
        int luhmSum = 0;
        for (int i = chs.length - 1, j = 0; i >= 0; i--, j++) {
            int k = chs[i] - '0';
            if (j % 2 == 0) {
                k *= 2;
                k = k / 10 + k % 10;
            }
            luhmSum += k;
        }
        return (luhmSum % 10 == 0) ? '0' : (char) ((10 - luhmSum % 10) + '0');
    }

    /**
     * 通过银行 的前六位确定 判断银行开户行及种
     */
    public static String getNameOfBank(String cardbin) {
        if(TextUtils.isEmpty(cardbin) || cardbin.length() < 6){
            return "";
        }
        // 通过银行的前6位确定
        cardbin = cardbin.substring(0, 6);
        int index = -1;
        for (int i = 0; i < BANKBIN.length; i++) {
            if (cardbin.equals(BANKBIN[i])) {
                index = i;
            }
        }
        if (index == -1) {
            return "";
        }
        return BANKNAME[index];
    }

